package com.fatwire.cs.rest.xom;

public class XOMException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6542450237105504019L;

	public XOMException() {
		super();
	}

	public XOMException(String message, Throwable cause) {
		super(message, cause);
	}

	public XOMException(String message) {
		super(message);
	}

	public XOMException(Throwable cause) {
		super(cause);
	}

}
